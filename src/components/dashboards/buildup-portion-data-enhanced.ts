/**
 * Built-up Portion Data for GIS Property Tax Dashboard
 * 
 * This file contains comprehensive building footprint data for all 6 wards in Thane Municipal Corporation.
 * Each polygon represents a built-up property with detailed information including:
 * - Property identification (Digipin, property number)
 * - Geographic coordinates (polygon boundaries)
 * - Property classification (type, sub-type, land use)
 * - Tax and assessment information
 * - Owner details
 * - Building characteristics
 */

export interface BuildupPortionPolygon {
  id: string;
  ward: string;
  digipin: string;
  propertyNo: string;
  coords: [number, number][];
  type: 'residential' | 'commercial' | 'industrial' | 'institutional' | 'vacant';
  subType: string;
  landUse: string;
  taxAmount: number;
  assessedValue: number;
  status: 'paid' | 'pending' | 'partial' | 'overdue';
  ownerName: string;
  area: number; // in sq meters
  floors: number;
  yearBuilt: number;
  lastPayment?: string;
  outstandingAmount?: number;
}

/**
 * Get color for different building types
 */
export function getBuildupTypeColor(type: string): string {
  const colors: Record<string, string> = {
    residential: '#3B82F6',
    commercial: '#8B5CF6',
    industrial: '#F59E0B',
    institutional: '#10B981',
    vacant: '#6B7280'
  };
  return colors[type] || '#94A3B8';
}

/**
 * Get label for building sub-types
 */
export function getBuildupSubTypeLabel(subType: string): string {
  const labels: Record<string, string> = {
    apartment: 'Apartment Complex',
    bungalow: 'Independent Bungalow',
    row_house: 'Row House',
    shop: 'Shop',
    office: 'Office Building',
    mall: 'Shopping Mall',
    factory: 'Factory',
    warehouse: 'Warehouse',
    school: 'Educational Institution',
    hospital: 'Healthcare Facility',
    temple: 'Religious Institution',
    plot: 'Vacant Plot'
  };
  return labels[subType] || subType;
}

